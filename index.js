function add(a,b){
  number = a + b;
  return number;
}
function subtract(a,b) {
  number = a - b;
  return number;
}
function multiply(a,b) {
  number = a * b;
  return number;
}
function divide(a,b) {
  number = a / b;
  return number;
}
